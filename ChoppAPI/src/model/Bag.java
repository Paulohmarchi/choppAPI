package model;

public class Bag {
	
	private Integer id;
	private Integer idProductValue;
	private Integer idProduct;
	private Integer idCustumer;
	private Integer amount;
	private Double discounts;
	private Double finalValue;
	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getIdProductValue() {
		return idProductValue;
	}
	public void setIdProductValue(Integer idProductValue) {
		this.idProductValue = idProductValue;
	}
	public Integer getIdProduct() {
		return idProduct;
	}
	public void setIdProduct(Integer idProduct) {
		this.idProduct = idProduct;
	}
	public Integer getIdCustumer() {
		return idCustumer;
	}
	public void setIdCustumer(Integer idCustumer) {
		this.idCustumer = idCustumer;
	}
	public Integer getAmount() {
		return amount;
	}
	public void setAmount(Integer amount) {
		this.amount = amount;
	}
	public Double getDiscounts() {
		return discounts;
	}
	public void setDiscounts(Double discounts) {
		this.discounts = discounts;
	}
	public Double getFinalValue() {
		return finalValue;
	}
	public void setFinalValue(Double finalValue) {
		this.finalValue = finalValue;
	}
	
	

}
