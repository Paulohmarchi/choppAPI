package model;

public class Trolley {
	
	private Integer id;
	private Integer idBag;
	private Integer idCustomerBag;
	private Double discounts;
	private Double finalValue;
	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getIdBag() {
		return idBag;
	}
	public void setIdBag(Integer idBag) {
		this.idBag = idBag;
	}
	public Integer getIdCustomerBag() {
		return idCustomerBag;
	}
	public void setIdCustomerBag(Integer idCustomerBag) {
		this.idCustomerBag = idCustomerBag;
	}
	public Double getDiscounts() {
		return discounts;
	}
	public void setDiscounts(Double discounts) {
		this.discounts = discounts;
	}
	public Double getFinalValue() {
		return finalValue;
	}
	public void setFinalValue(Double finalValue) {
		this.finalValue = finalValue;
	}
	
	
	
}
